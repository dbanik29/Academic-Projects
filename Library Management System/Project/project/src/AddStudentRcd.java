    private void Update_table() {                                     
        // TODO add your handling code here:
        
        try{
            String sql = "select Stu_ID,Stu_Name,Gender,Phn_No,Address,Email from InfoStu";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            tablett.setModel(DbUtils.resultSetToTableModel(rs));
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{
                rs.close();
                pst.close();
            }catch(Exception e){
                
            }
        }         
    }

    private void Fillcombo() {                                     
        try{
            String sql = "select from InfoStu";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
               String Gender = rs.getString("Gender");
               ComboBox.addItem(Gender);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{
                rs.close();
                pst.close();
            }catch(Exception e){
                
            }
        } 
        Update_table();
    }

