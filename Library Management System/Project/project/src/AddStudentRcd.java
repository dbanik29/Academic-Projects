    private void savestudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savestudentActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "Insert into InfoStu (Stu_ID,Stu_Name,Gender,Phn_No,Address,Email) values (?,?,?,?,?,?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1,id.getText());
            pst.setString(2, name.getText());
            pst.setString(3, ComboBox.getSelectedItem().toString());
            pst.setString(4, phnno.getText());
            pst.setString(5, address.getText());
            pst.setString(6, email.getText());
            
            pst.execute();
            JOptionPane.showMessageDialog(null, "Saved");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        Update_table();
    }//GEN-LAST:event_savestudentActionPerformed

